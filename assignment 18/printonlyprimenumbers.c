#include <stdio.h>
#include <stdlib.h>

int isPrime(int num) {
    if(num < 2) return 0;
    for(int i = 2; i <= num/2; i++)
        if(num % i == 0)
            return 0;
    return 1;
}

int main() {
    int n;
    scanf("%d", &n);

    int *arr = malloc(n * sizeof(int));

    for(int i = 0; i < n; i++)
        scanf("%d", &arr[i]);

    printf("Prime numbers:\n");
    for(int i = 0; i < n; i++)
        if(isPrime(arr[i]))
            printf("%d ", arr[i]);

    free(arr);
    return 0;
}